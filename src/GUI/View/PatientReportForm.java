    public void loadComboBox() {
        try {
            List<Report> reports = rr.findAll();
            ArrayList<Report> arrList = new ArrayList<>();
            for (int i = 0; i < reports.size(); i++) {
                if (reports.get(i).getPatientID().getPatientID() == this.patientID_Table) {
                    arrList.add(reports.get(i));
                }

            }
            crcb.add(arrList);
            comboBox.setModel(crcb);
            comboBox.repaint();
        } catch (HealthException ex) {
            JOptionPane.showMessageDialog(this, "Info:" + ex.getMessage());
        }
    }
    
