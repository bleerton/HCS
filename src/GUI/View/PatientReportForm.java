    public void loadLabels() throws HealthException {
        if (!hasReports()) {
            JOptionPane.showMessageDialog(this, "No Report is available!");
            throw new HealthException("No report is registered with this patient !");
        }
        try {
            Report r = (Report) comboBox.getSelectedItem();
            System.out.println(r);
            patientIDText.setText(r.getPatientID() + "");
            patientNameText.setText(r.getPatientID().getFirstName());
            patientLastNameText.setText(r.getPatientID().getLastName());
            parentNameText.setText(r.getPatientID().getParentName());
            patientAddressText.setText(r.getPatientID().getAddress());
            if (r.getPatientID().getSex().equalsIgnoreCase("Female")) {
                femaleButton.setSelected(true);
                maleButton.setSelected(false);
            } else {
                maleButton.setSelected(true);
                femaleButton.setSelected(false);
            }
            doctorIDText.setText(r.getDoctorID() + "");
            doctorLastName.setText(r.getDoctorID().getLastName());
            doctorNameText.setText(r.getDoctorID().getFirstName());
            doctorPhoneNumberText.setText(r.getDoctorID().getPhoneNumber());
            cityText.setText(r.getCity());
            codeText.setText(r.getCode()+"");
            if (r.getInstitution().equalsIgnoreCase("Public")) {
                publicRadioBox.setSelected(true);
                privateRadioBox.setSelected(false);
            } else {
                privateRadioBox.setSelected(true);
                publicRadioBox.setSelected(false);
            }
            diagnosisText.setText(r.getDiagnose());
        } catch (NullPointerException ex) {
        }
    }

    public boolean hasReports() throws HealthException{
        List<Report> reports = rr.findAll();
        for (Report report : reports) {
            if (report.getPatientID().getPatientID() == this.getPatientIDTable()) {
                return true;
            }
        }
        return false;
    }
