    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        if (annyIsEmpty()) {
            JOptionPane.showMessageDialog(this, "White space not allowed");
            return;
        }

        if (PatientOrDoctor == 2) {
            Login l = new Login();
            Doctor d = new Doctor();

            d.setFirstName(FirstNameText.getText());
            d.setLastName(lastNameText.getText());
            d.setSpecalization(specializationText.getText());
            d.setEmail(emailText.getText());
            d.setAddress(addressText.getText());
            d.setPhoneNumber(phoneNrText.getText());
            try {
                d.setDateOfBirth(setTime(dateChooser.getCurrent()));
            } catch (ParseException ex) {
                Logger.getLogger(AddDoctorOrPatientFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (maleButton.isSelected()) {
                femaleButton.setSelected(false);
                d.setSex("Male");
            } else if (femaleButton.isSelected()) {
                maleButton.setSelected(false);
                d.setSex("Female");
            } else {
                JOptionPane.showMessageDialog(this, "Gender is not selected");
                return;
            }

            try {
                d.setUsername(usernameText.getText());
                l.setUsername(usernameText.getText());
                if (!turnPasswordFieldToString(passwordField).equalsIgnoreCase(turnPasswordFieldToString(repeatPasswordLabel))) {
                    JOptionPane.showMessageDialog(this, "Passwords dont match !");
                    passwordField.setText("");
                    repeatPasswordLabel.setText("");
                } else {
                    d.setPassword(new String(passwordField.getPassword()));
                    l.setPassword(new String(passwordField.getPassword()));
                }
                l.setRoli(2);
                lr.create(l);
                d.setLoginID(l);
                dr.create(d);
                JOptionPane.showMessageDialog(this, "Doctor Registered succesfully!");
                this.dispose();

            } catch (HealthException ex) {
                Logger.getLogger(AddDoctorOrPatientFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (PatientOrDoctor == 3) {
            Login l = new Login();
            Patient p = new Patient();
            p.setFirstName(FirstNameText.getText());
            p.setLastName(lastNameText.getText());
            p.setParentName(specializationText.getText());
            p.setEmail(emailText.getText());
            p.setAddress(addressText.getText());
            p.setPhoneNumber(phoneNrText.getText());
            try {
                p.setDateOfBirth(setTime(dateChooser.getCurrent()));
            } catch (ParseException ex) {
                Logger.getLogger(AddDoctorOrPatientFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (maleButton.isSelected()) {
                p.setSex("Male");
            } else if (femaleButton.isSelected()) {
                p.setSex("Female");
            } else {
                JOptionPane.showMessageDialog(this, "Gender is not selected");
            }

            try {
                p.setUsername(usernameText.getText());
                l.setUsername(usernameText.getText());
                if (!turnPasswordFieldToString(passwordField).equalsIgnoreCase(turnPasswordFieldToString(repeatPasswordLabel))) {
                    JOptionPane.showMessageDialog(this, "Passwords dont match !");
                    passwordField.setText("");
                    repeatPasswordLabel.setText("");
                } else {
                    p.setPassword(new String(passwordField.getPassword()));
                    l.setPassword(new String(passwordField.getPassword()));
                }
                l.setRoli(3);
                lr.create(l);
                p.setLoginId(l);
                pr.create(p);
                JOptionPane.showMessageDialog(this, "Patient created succesfully !");
                this.dispose();
            } catch (HealthException ex) {
                Logger.getLogger(AddDoctorOrPatientFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Can not define if it s a doctor or patient.");
        }

    }//GEN-LAST:event_createButtonActionPerformed

