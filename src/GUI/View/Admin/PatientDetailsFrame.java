    public void loadTable() {
        try {
            List<Patient> listByID = pr.findAll();
            ptm.addList(listByID);
            table.setModel(ptm);
            ptm.fireTableDataChanged();
        } catch (HealthException ex) {
            JOptionPane.showMessageDialog(this, "Info:" + ex.getMessage());
        }
    }

    public void loadTableFromID(int id) {
        try {
            List<Patient> list = pr.findAll();
            ArrayList<Patient> listByID = new ArrayList<>();
            for (Patient patient : list) {
                if (patient.getPatientID() == id) {
                    listByID.add(patient);
                }
            }
            if (listByID.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Patient with the given ID doesnt exist.");
                loadTable();
            }
            ptm.addList(listByID);
            table.setModel(ptm);
            ptm.fireTableDataChanged();
        } catch (HealthException ex) {
            JOptionPane.showMessageDialog(this, "Info:" + ex.getMessage());
        }
    }

