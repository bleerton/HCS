    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int row = table.getSelectedRow();
        if (row != -1) {
            Object[] ob = {"Po", "Jo"};
            int i = JOptionPane.showOptionDialog(this, "Are you sure you want to delete this patient?", "Patient deletion",
                    JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if (i == 0) {
                Patient p = ptm.getPatient(row);
                try {
                    checkForErrorsAtConnections(p);
                    checkForErrorsAtAppointments(p);
                    pr.delete(p);
                    checkForErrorsAtLogin(p);
                } catch (HealthException ex) {
                    JOptionPane.showMessageDialog(this, "Couldnt delete patient, ERROR: " + ex.getMessage());
                    return;
                }

                loadTable();
            } else {
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire!");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    public void checkForErrorsAtLogin(Patient p) throws HealthException {
        List<Login> logins = lr.findAll();
        for (int j = 0; j < logins.size(); j++) {
            if (Objects.equals(logins.get(j).getLoginID(), p.getLoginId().getLoginID())) {
                lr.delete(logins.get(j));
            }
        }
    }

    //Metod qe se len ndatabaz me pas konflikt permes ID kur don me fshi ni pacient
    public void checkForErrorsAtConnections(Patient p) throws HealthException {
        List<Connection> connections = cr.findAll();
        for (int j = 0; j < connections.size(); j++) {
            if (Objects.equals(connections.get(j).getPatientID().getPatientID(), p.getPatientID())) {
                cr.delete(connections.get(j));
            }
        }
    }

    //Metod qe se len ndatabaz me pas konflikt permes ID kur don me fshi ni pacient
    public void checkForErrorsAtAppointments(Patient p) throws HealthException {
        List<Appointment> appointments = ar.findAll();
        for (int j = 0; j < appointments.size(); j++) {
            if (Objects.equals(appointments.get(j).getPatientID().getPatientID(), p.getPatientID())) {
                ar.delete(appointments.get(j));
            }
        }
    }

